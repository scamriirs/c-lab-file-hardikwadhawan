1.Write program to demonstrate the working of c# SDK

using System;

class Program

{

static void Main()

{

Console.WriteLine("Hello, World!");

}

}

Explanation:

using System;: This includes the System namespace, which contains fundamental classes like Console.

class Program: Defines the class named Program.

static void Main(): The Main method is the entry point of any C# console application. It is where the program starts execution.

Console.WriteLine("Hello, World!");: This line outputs the text "Hello, World!" to the console.